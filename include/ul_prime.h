/**
 *  组装一段数据的64位数据签名的第1部分
 *
 *  @param[in]  str  源data
 *  @param[in]  slen data长度
 *  @param[out] 无
 *  @return 64位签名的第1部分32位int
 */
uint32_t getsigns_24_1(const char* str, int slen);

/**
 *  组装一段数据的64位数据签名的第2部分
 *
 *  @param[in]  str  源data
 *  @param[in]  slen data长度
 *  @param[out] 无
 *  @return 64位签名的第2部分32位int
 */
uint32_t getsigns_24_2(const char* str, int slen);

#endif